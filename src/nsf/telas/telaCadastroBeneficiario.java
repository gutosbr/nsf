    public void alterarBeneficiario() throws SQLException{
        if(JTTabelaBeneficiario.getSelectedRow() != -1){
            if(verificaDados()){
                Beneficiario bf = new Beneficiario();
                BeneficiarioDao dao = new BeneficiarioDao();
                bf.setBf_id(Integer.valueOf(jtid.getText()));
                bf.setBf_nome(jtnome.getText());
                bf.setBf_cpf(jtcpf.getText());
                bf.setBf_rg(jtrg.getText());
                bf.setBf_endereco(jtendereco.getText());
                bf.setBf_bairro(jtbairro.getText());
                bf.setBf_cidade(jtcidade.getText());
                bf.setBf_estado(jtestado.getText());
                bf.setBf_fone(jtcontato.getText());
                bf.setBf_obs(jtobs.getText());
                dao.alterar(bf);
                JOptionPane.showMessageDialog(null, "Registro ALTERADO com SUCESSO !S");
            }
        }
    }
    
    public void excluirBeneficiario() throws SQLException {
        int resp = JOptionPane.showConfirmDialog(this, "Deseja realmente EXCLUIR esse registro ? ",
                "Confirmação", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_NO_OPTION) {
            BeneficiarioDao dao = new BeneficiarioDao();
            dao.remove(beneficiarios.get(JTTabelaBeneficiario.getSelectedRow()));
        }
    }

