    private void mostrarPesquisa(List<Beneficiario> beneficiarios) {
        while(tmBeneficiario.getRowCount() > 0 ){
            tmBeneficiario.removeRow(0);
        }
       if(beneficiarios.size() == 0 ){
           JOptionPane.showMessageDialog(null, "Nenhum registro cadastrado!");
    }else{
           String[] linha = new String[] {null, null, null, null, null, null, null, null, null};
           for (int i = 0; i <  beneficiarios.size(); i++){
               tmBeneficiario.addRow(linha);
               tmBeneficiario.setValueAt(beneficiarios.get(i).getBf_id(),  i,  0);
               tmBeneficiario.setValueAt(beneficiarios.get(i).getBf_nome(),  i,  1);
               tmBeneficiario.setValueAt(beneficiarios.get(i).getBf_cpf(),  i,  2);
               tmBeneficiario.setValueAt(beneficiarios.get(i).getBf_rg(),  i,  3);
               tmBeneficiario.setValueAt(beneficiarios.get(i).getBf_endereco(),  i,  4);
               tmBeneficiario.setValueAt(beneficiarios.get(i).getBf_bairro(),  i,  5);
               tmBeneficiario.setValueAt(beneficiarios.get(i).getBf_cidade(),  i,  6);
               tmBeneficiario.setValueAt(beneficiarios.get(i).getBf_estado(),  i,  7);
               tmBeneficiario.setValueAt(beneficiarios.get(i).getBf_fone(),  i,  8);
               tmBeneficiario.setValueAt(beneficiarios.get(i).getBf_obs(),  i,  9);
               
           }
       }}
